declare(strict_types = 1);

namespace Test\Unit\Event\Company;

use App\Entity\Company;
use App\Event\Company\Updated;
use Jenssegers\Optimus\Optimus;
use Test\Unit\AbstractUnit;

class UpdatedTest extends AbstractUnit {
    public function testConstruct() {
        $optimus = $this->getMockBuilder(Optimus::class)
            ->disableOriginalConstructor()
            ->getMock();

        $company = new Company([], $optimus);

        $updated = new Updated($company);

        $this->assertInstanceOf(Company::class, $updated->company);
    }
}
